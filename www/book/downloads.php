<p class="articleTitle">Download Options</p>
<blockquote>

<p>
You can get a PDF copy (in color) of the published edition of <span class="name">IPSUR</span> from CRAN, and you can get a PDF of the development edition from R-Forge;  see the the <a href="installation.php">Installation</a> page for details.  Note that the <span class="name">Sweave</span> sources of both of those documents are stored alongside their PDF siblings.
</p>

<p>
If you would like to print your own copy of the <strong>published</strong> edition of <span class="name">IPSUR</span> then you may want to use the following publication-ready PDF of the book.  It has been typeset in black-and-white to optimize the code display. All of the fonts have been embedded so your printer should not have trouble with any of the symbols.
</p>

<p>
<div align="center">
	<strong>Publisher Quality PDF</strong><br />
	<a href="download/IPSUR.pdf">IPSUR.pdf</a>
</div>
</p>

<p>
If you would like a transparent copy of the published edition of <span class="name">IPSUR</span>, then download the following <span class="name">zip</span> file.  It contains all of the LaTeX source code and all of the figures (in <span class="name">.pdf</span> and <span class="name">.eps</span>).  These can be used (in principle) to generate your own personal copy of the published edition of <span class="name">IPSUR</span>, and you are FREE to modify and distribute your own version of it under the constraints of the GNU-FDL.
</p>

<p>
<div align="center">
	<strong>LaTeX source and Images</strong><br />
	<a href="download/IPSUR.zip">IPSUR.zip</a>
</div>
</p>

<p>
Finally, you can download the LyX source file, <span class="name">IPSUR.lyx</span>, which was used by the author to write <span class="name">IPSUR</span>. It is essentially the <span class="name">Sweave</span> source code for the published edition, but LyX conceals the intermediate step of generating the LaTeX source file and figures.  This one text file will generate a PDF of the entire published edition of <span class="name">IPSUR</span> with a single click.  Enjoy.  (I certainly have.)
</p>

<p>
<div align="center">
	<strong>Master LyX File</strong><br />
	<a href="download/IPSUR.lyx">IPSUR.lyx</a>
</div>
</p>

<p>
You will need the <a href="download/IPSUR.bib">BibTeX</a> file to generate the bibliography.
</p>

<p>
You are free to print your own copy of the <em>development</em> edition of <span class="name">IPSUR</span>, but for several reasons I do not recommend it.  For one thing, the electronic copy is in color which does not show up well on black-and-white paper.  For another, the electronic copy is optimized for web viewing which means that not all of the fonts are embedded in the PDF - your printer may not render all of the symbols correctly.  Moreover, unless you have a very fancy printer you will be printing only on one side of the page - which wastes paper.
</p>

<p>
If you would like to print your own copy of the <span class="name">IPSUR</span> development edition then the best approach would be to download the <span class="name">Sweave</span> source, compile your own PDF in black-and-white, and be sure to embed all of the fonts. <a href="download/generateIPSUR">Here</a> is a script that I use to generate <span class="name">IPSUR</span> for publication.
</p>

