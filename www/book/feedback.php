<p class="articleTitle">Giving Feedback</p>
<blockquote>

<p>
I would be happy to hear any (most?) comments, suggestions, problems, questions, or requests that you may have about <span class="name">IPSUR</span>. But if you would like to ask/say something, then please ask/say it on the <span class="name">IPSUR</span> mailing lists (or my personal address), and refrain from bogging down the already busy R-help mailing list.  I have made two <span class="name">IPSUR</span> specific mailing lists for this purpose:   
</p>

<p class="articleTitle">Mailing Lists</p>
<blockquote>
<p> 
  If you have a question about how to use, install, download, or any other operation problem, then I invite you to use the <span class="name">IPSUR</span>-help list at <a href="mailto:ipsur-help@lists.r-forge.org">ipsur-help@lists.r-forge.org</a>.  It is a moderated list, which means that you need to register to be able to post messages freely.  If you are not registered then every message  you send will need to be approved in advance (by me) before it will be posted to the list and stored in the archives. Visit here for registration instructions;  don't worry, it is quick and painless.
</p>
<p>
  Please note that <strong>questions about the exercises</strong> are <strong>intentionally deleted</strong> from the list of invited questions.  I <strong>will not</strong> answer questions about the exercises (or other conceptual matters) unless you <em>happen</em> to catch me on a very, very good day.  You should already have your own professor to teach you probability and statistics and (s)he is the one being paid to do it, not me.
</p>

<p>
If you find a mistake in the narrative, if you have a suggestion regarding the subject matter, or if you have <em>constructive</em> criticism of the book, then I invite you to use the <span class="name">IPSUR</span>-devel list at <a href="mailto:ipsur-devel@lists.r-forge.org">ipsur-devel@lists.r-forge.org</a>.  This is also a moderated list;  go here to register.  Before writing please bear in mind that I do pledge to amicably listen, but I do not promise to agree, with you.  Moreover, the fact that <span class="name">IPSUR</span> is FREE means that if you don't like something then you are FREE to fix it yourself.  All that being said, I would be happy to hear about your experiences with the book, good or bad, in the classroom or elsewhere. 
</p>
<p>
If you prefer to write a private message then you can reach me at <a href="mailto:gkerns@ysu.edu">gkerns@ysu.edu</a> (<a href="http://people.ysu.edu/~gkerns">home page</a>).<br />
</p>

