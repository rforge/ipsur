<p class="articleTitle">Giving Feedback</p>
<blockquote>

<p>
I would be happy to hear any (most?) comments, suggestions, problems, questions, or requests that you may have about <span class="name">IPSUR</span>. And if you would like to ask/say something, then it would be better to ask/say it on the <span class="name">IPSUR</span> mailing lists (or my personal address) rather than bog down an already busy R-help mailing list.  There are two <span class="name">IPSUR</span> specific mailing lists for this purpose.
</p>
</blockquote>


<p class="articleTitle">Mailing Lists</p>
<blockquote>
<p> 
  If you have a question about how to use, install, download, or otherwise operate <span class="name">IPSUR</span>, then write to the <span class="name">IPSUR</span>-help list at <a href="mailto:ipsur-help@lists.r-forge.org">ipsur-help@lists.r-forge.org</a>.  It is a moderated list, which means that you need to register to be able to post messages freely.  If you are not registered then every message  you send will need to be approved in advance (by me) before it will be posted to the list and stored in the archives. Visit <a href="https://lists.r-forge.r-project.org/cgi-bin/mailman/listinfo/ipsur-help">here</a> for registration instructions;  don't worry, it is quick and painless.
</p>
<p>
  Please note that <strong>questions about the exercises</strong> are <strong>intentionally deleted</strong> from the list of appropriate questions.  I <strong>will not</strong> answer questions about the exercises (or other conceptual matters), period.  You should already have your own professor to teach you probability and statistics, and (s)he is the one being paid to do it, not me.
</p>

<p>
If you find a mistake in the narrative, if you have a suggestion regarding the subject matter, or if you have <em>constructive</em> criticism of the book, then please let me know on the <span class="name">IPSUR</span>-devel list at <a href="mailto:ipsur-devel@lists.r-forge.org">ipsur-devel@lists.r-forge.org</a>.  This is also a moderated list;  go <a href="https://lists.r-forge.r-project.org/cgi-bin/mailman/listinfo/ipsur-devel">here</a> to register.  Before writing please bear in mind that regarding subjective matters I do pledge to amicably listen to - but I do not promise to agree with - everybody about everything.  Moreover, the fact that <span class="name">IPSUR</span> is FREE means that if you don't like something then you are FREE to rewrite it yourself.  All that being said I would be happy to hear about your experiences with the book, good or bad, in the classroom or elsewhere. 
</p>
<p>
If you prefer to write a private message then you can reach me at <a href="mailto:gkerns@ysu.edu">gkerns@ysu.edu</a> (<a href="http://people.ysu.edu/~gkerns">home page</a>).<br />
</p>
</blockquote>


